function greet(name) {
    Console.log("Hello, " + name)
}
greet("John")

/**
 * In this example, "John" is the argument and name is the parameter. When the greet function is called with the argument "John", the value of "John" is passed to the parameter name, which is then used to print the greeting.
 */

function functionWithArgs(num1, num2) {
    console.log(num1 + num2);
}

functionWithArgs(1, 2);

